namespace ArithmeticChallenge
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   (Serializable) an equations. </summary>
    ///
    /// <remarks>   Galarist, 18/09/2018. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    [Serializable]
    class Equations
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the number of. </summary>
        ///
        /// <value> The f number. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [JsonProperty("first_number")]
        public int FNumber { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the number of. </summary>
        ///
        /// <value> The s number. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [JsonProperty("second_number")]
        public int SNumber { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the operator. </summary>
        ///
        /// <value> The operator. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [JsonProperty("symbol")]
        public string Operator { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the number of. </summary>
        ///
        /// <value> a number. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [JsonProperty("result")]
        public int ANumber { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets a value indicating whether this object is correct. </summary>
        ///
        /// <value> True if this object is correct, false if not. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [JsonProperty("is_correct")]
        public bool IsCorrect { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Default constructor. </summary>
        ///
        /// <remarks>   Galarist, 18/09/2018. </remarks>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public Equations() { }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Constructor. </summary>
        ///
        /// <remarks>   Galarist, 18/09/2018. </remarks>
        ///
        /// <param name="firstNumber">  The first number. </param>
        /// <param name="secondNumber"> The second number. </param>
        /// <param name="symbol">       The symbol. </param>
        /// <param name="result">       The result. </param>
        /// <param name="isCorrect">    True if is correct, false if not. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public Equations(int firstNumber, int secondNumber, string symbol, int result, bool isCorrect)
        {
            FNumber = firstNumber;
            SNumber = secondNumber;
            Operator = symbol;
            ANumber = result;
            IsCorrect = isCorrect;
        }
    }
}
